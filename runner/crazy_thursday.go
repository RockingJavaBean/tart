// crazyThursdayBanner since we are building our own runner,
// we may add some meme we like.
func crazyThursdayBanner() string {
	return color.HiYellowString(crazyArt) + "\n" + color.HiGreenString(vMeArt) + "\n\n"
}

func isTodayThursday() bool {
	return time.Now().Weekday() == time.Thursday
}
